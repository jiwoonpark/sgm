package com.spring.main.dto;

public class FileDto {
	private String f_idx;
	private String j_idx;
	private String f_category;
	private String f_newfilename;
	private String f_filename;
	
	public String getF_idx() {
		return f_idx;
	}
	public void setF_idx(String f_idx) {
		this.f_idx = f_idx;
	}
	public String getJ_idx() {
		return j_idx;
	}
	public void setJ_idx(String j_idx) {
		this.j_idx = j_idx;
	}
	public String getF_category() {
		return f_category;
	}
	public void setF_category(String f_category) {
		this.f_category = f_category;
	}
	public String getF_newfilename() {
		return f_newfilename;
	}
	public void setF_newfilename(String f_newfilename) {
		this.f_newfilename = f_newfilename;
	}
	public String getF_filename() {
		return f_filename;
	}
	public void setF_filename(String f_filename) {
		this.f_filename = f_filename;
	}

}
